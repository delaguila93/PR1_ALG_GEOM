public class DoubleType {

    /**
     * @return the v
     */
    public double getV() {
        return v;
    }

    /**
     * @param v the v to set
     */
    public void setV(double v) {
        this.v = v;
    }
    private double v;

    public DoubleType() {
        v=0;
    }

    public DoubleType(double va) {
        v = va;
    }

}
